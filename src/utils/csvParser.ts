/**
 * 指定ロールのスキルを取得する
 */
export const getSkillsByRole = (skills: Skill[], role: string): Skill[] => {
  return skills.filter(skill => skill.ロール === role);
};

/**
 * ロール名 → スキル数 のマップを返す
 */
export const getSkillCountByRole = (skills: Skill[]): Record<string, number> => {
  const counts: Record<string, number> = {};
  skills.forEach(skill => {
    counts[skill.ロール] = (counts[skill.ロール] || 0) + 1;
  });
  return counts;
};
